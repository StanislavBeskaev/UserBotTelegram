import random

import pyrogram

from app.commands.base_command import BaseDotCommand


class LikeCommand(BaseDotCommand):
    """Команда для рисования пальца вверх"""

    @property
    def text(self) -> str:
        return "like"

    @property
    def description(self) -> str:
        return "Команда для рисования пальца вверх (разные рисунки, выбираются случайный)"

    def execute(self, client: pyrogram.client.Client, message: pyrogram.types.Message) -> None:
        available_likes = [getattr(self, prop) for prop in dir(self) if prop.startswith("like")]
        chosen_like = random.choice(available_likes)
        like_str = f"`{chosen_like}`"
        message.edit(like_str)

